package day17_loops;

public class ForLoopExample {
    public static void main(String[] args) {

        for(int i = 1; i <= 10; i++ ){
            System.out.println(i);
        }

        // If you did the same code as above with while loop:

        int z = 1;

        while(z <= 10){
            System.out.println(z);
            z++;
        }

    }
}
